document.addEventListener("DOMContentLoaded", () => {
  console.log("This site was generated by Hugo.");

  const header = document.getElementById("js-global-header");
  if (header) {
    getElementHeight(header, "--header-height");
  }

  const menu = document.getElementById("js-drawerMenu");
  const openMenu = document.getElementById("js-openMenu");
  const closeMenu = document.getElementById("js-closeMenu");

  if (menu && openMenu && closeMenu) {
    openMenu.addEventListener("click", () => menu.showModal());
    closeMenu.addEventListener("click", () => menu.close());
  }

  switchTheme();
});

/* 特定の要素の高さを取得し、カスタムプロパティに登録する */
function getElementHeight(targetElement, propertyName) {
  if (targetElement == null) return;

  const heightObserver = new ResizeObserver((entries) => {
    const entry = entries[0];

    if (entry.borderBoxSize) {
      const { blockSize } = entry.borderBoxSize[0];
      const roundedHeight = Math.round(blockSize) + "px";

      document.documentElement.style.setProperty(propertyName, roundedHeight);
    }
  });

  heightObserver.observe(targetElement);
}

/* ダークモード・ライトモードを切り替える */
function switchTheme() {
  const toggleButton = document.getElementById("js-toggleTheme");

  if (toggleButton) {
    toggleButton.addEventListener("click", () => {
      const currentTheme = document.documentElement.getAttribute("data-theme");
      const themeToApply = currentTheme === "dark" ? "light" : "dark";

      sessionStorage.setItem("theme", themeToApply);
      if ( themeToApply === "dark" ) {
        document.documentElement.dataset.theme = "dark";
      } else {
        document.documentElement.dataset.theme = "light";
      }
    });
  }
}
